        <div class="container">
            <span>Servicio de pulverización en frío (desinsectación)</span>
            <br><br>
            Luego del control y revisión que debe realizar el inspector de ISCAMEN, los vehículos reciben un tratamiento
            de pulverización en frío.
            <br><br>
            La pulverización en frío de vehículos se realiza con un sistema automático que aplica una solución con
            insecticida de toxicidad prácticamente nula para animales de sangre caliente pero de  alta especificidad
            para insectos. Se realiza por medio de picos que se ubican a nivel del suelo y "tiran" hacia arriba para el
            caso de automóviles, y también con picos laterales para los vehículos de carga.
            <br><br>
            La identificación del tipo de vehículo es realizada por un grupo de sensores infrarrojos que determina, para
            cada caso, con qué picos se realiza la pulverización.
            La presión del insecticida se logra a través de bombas que son alimentadas por tanques donde se realiza la
            mezcla, consistente en 40 cc de Cipermetrina en cada 200 litros de mezcla.
            <br><br>
            En muchas oportunidades, puede ocurrir que los conductores de automóviles no adviertan el trabajo realizado,
            ya que es por la parte inferior del vehículo, por ello en el sector de pulverización en frío, se ha
            instalado cartelería específica que indica el lugar exacto donde se realiza la pulverización de los
            vehículos, la obligación de reducir la velocidad de circulación e instrucciones de cerrar las ventanillas
            para evitar el ingreso del producto al vehículo, donde por lo general los pasajeros transitan ingiriendo
            alimentos, como galletas, mate, etc.
            <br><br>
            Para advertir el trabajo basta con detenerse a observar cuando pase un automóvil, momento en el que se
            inicia el proceso.
