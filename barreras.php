                <br><br>
                <ul>
                    <li><span>Automóviles:</span> se solicita al conductor la apertura del baúl, de bolsos, valijas,
                        cajas, canastas, conservadoras, etc., a efectos de constatar que pudieran contener vegetales
                        hospederos de las moscas de los frutos. Posteriormente, se observa el interior del vehículo
                        debajo de los asientos o en otro habitáculo.</li>
                    <br>
                    <li><span>Camionetas: </span> se solicita que abran la baulera (que usualmente transportan en la
                        caja), bolsos, valijas, cajas, conservadoras, etc. Luego se pide que desplacen el respaldo para
                        observar si no transportan vegetales hospederos u otro producto que se controle en Barreras. En
                        caso que la camioneta contenga lona plana, se solicita que se la levante para inspeccionar la
                        caja.</li>
                    <br>
                    <li><span>Casillas rodantes, lanchas u otros vehículos de arrastre: </span>se inspeccionan los
                        equipajes,armarios, alacenas, bauleras, hornos de cocinas, heladeras, recipientes deresiduos y
                        cualquier otro compartimento que pudiera poseer vegetaleshospederos.</li>
                    <li><span>Transporte público de pasajeros: </span>el inspector asciende al vehículo e inspecciona
                        cada uno de los buches, debajo de los asientos, pasillos, bolsos y canastas de mano. Luego
                        inspecciona las bodegas, verificando si algún equipaje contiene vegetales hospederos,
                        solicitando al dueño la apertura de los mismos. Además, se inspeccionan las encomiendas
                        transportadas.</li>
                    <br>
                    <li>
                        Camionesen general: en caso que el camión no contenga carga alguna, se procede ainspeccionar la
