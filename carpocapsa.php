                <h3>Control cultural</h3>

                Promover y apoyar el uso de medidas culturales y manejo del monte tendientes a disminuirlos los niveles
                poblacionales de estas plagas, tales como:

                <ul>
                    <li>Uso de bandas trampa.</li>
                    <br>
                    <li>Recolección y destrucción de frutos que pudiesen haber quedado después de la cosecha.</li>
                    <br>
                    <li>Podas de formación donde las plantas no excedan los 4,5 metros de altura, para que las
                        pulverizaciones cubran el total de la copa de los árboles con la solución de agroquímico y una
                        distribución de ramas que asegure la buena penetración de los productos.</li>
                    <br>
                    <li>Raleo de frutos de manera tal que no se toquen al aumentar de tamaño.</li>
                    <br>
                    <li>Calibración del equipo pulverizador por lo menos una vez por año. Para el caso, el Instituto
                        lleva
                        adelante diversos encuentros de capacitación.</li>
                    <br>
                    <li>Difusión de medidas para una correcta aplicación: la medición del pH del agua con la que se
                        prepara
                        el caldo, seleccionar productos insecticidas permitidos respetando la acción residual del mismo
                        y
                        sus tiempos de carencia, prepararlos de acuerdo a las dosis indicadas por el marbete del
                        producto a
                        utilizar, suspender las aplicaciones cuando la velocidad del viento supera los 4 metros por
                        segundo,
                        cuando la temperatura supera los 30°C o cuando la humedad relativa es menor a 35%, realizarlas
                        en
                        las últimas horas de la tarde o nocturnas, repetir los tratamientos después de una lluvia, etc.
                    </li>
                    <br>
                    <li>Uso de registros como los "Cuadernos de Campo".</li>
                    <br>
                    <li> Medidas preventivas, tales como retiro de bins, cajones, puntales, leña, entre otros elementos,
                        de
                        los montes para evitar reinfestación.</li>
                </ul>
                <span>¿Qué son las bandas trampas?</span>
                <br><br>
                Las bandas trampas son "tiras" de cartón corrugado, de entre 15 y 20 cm de ancho. Normalmente se
                utilizan
                los sobrantes del cartón empleado en el embalaje, ya que cualquier tipo de corrugado funciona como
                trampa
                para estas plagas. Cuando la larva de cualquiera de las dos plagas, se "tira" de la fruta para empupar,
                confunde la corteza del árbol con la trampa de cartón. Por eso es posible después de un tiempo,
                encontrar
                los cartones con larvas invernantes. Correctamente utilizadas, disminuyen la población de plagas
                presentes
                en el monte
                <br><br>
                <span>¿Cómo se colocan?</span>
                <br><br>
                <span>En la parcela:</span>
                <br><br>
                En las dos primeras hileras perimetrales de la parcela se colocan bandas en todos los árboles.Dentro de
                la
                parcela se colocan árbol por medio, hilera por medio.
                <br><br>
                <span>En el árbol:</span>
                <br>
                <ul>
                    <li>Esas "tiras" de cartón corrugado se colocan envolviendo el tronco del árbol a unos 30 cm del
                        suelo.
                        Deben dar dos vueltas al tronco y sostenerse con algún elemento que no lastime al árbol, como
                        por
                        ejemplo grampas.</li>
                    <br>
                    <li>Se recomienda colocarlas en enero.</li>
                    <br>
                    <li>Los cartones deben ser retirados y quemados en invierno, a partir del mes de mayo, antes de
                        agosto.
                    </li>
                </ul>
