    <div class="tituloPrincipal">
        <span>SANIDAD Y CALIDAD</span>
    </div>
    <div class="container">
        Para alcanzar los estándares de sanidad y calidad requeridos por los mercados se precisan esfuerzos, recursos y
        prácticas que abarcan a todos los actores involucrados en el proceso productivo agrícola. Cada país o región,
        busca proteger y mejorar sus condiciones de producción y sus estatus sanitarios.
        <br><br>
        <span>Las condiciones exigibles por los mercados son dinámicas y varían de acuerdo a los productos agrícolas
        ofrecidos, como así también a deseos de los consumidores, tanto internos como externos, y las demandas de cada
        región o país de destino. Las exigencias han ido creciendo a través de los años, incluso se han incrementado
        notablemente para el mercado interno.</span>
        <br><br>
        El comercio internacional está reglamentado por la Organización Mundial del Comercio (OMC) que adhiere a la
        Convención Internacional de Protección Fitosanitaria, de la cual Argentina es parte integrante. Cada país cuenta
        con un organismo nacional de protección fitosanitaria que acuerda con sus pares, las condiciones y protocolos
        para asegurar que los productos primarios que se exportan están libres de plagas agrícolas.
        <br><br>
        La presencia de insectos-plaga siempre ha requerido por parte de los productores el despliegue de prácticas de
        control y en no pocas oportunidades la organización y el establecimiento de instituciones específicas como por
        ejemplo las antiguas “Juntas” y “Comisiones” de defensa agrícola para el control de langostas. Asimismo, las
        herramientas han variado en el tiempo desde el uso de barreras naturales, lanzallamas o incluso fitosanitarios
        altamente tóxicos como el DDT, hasta técnicas que garanticen el control y al mismo tiempo la inocuidad
        alimentaria de los productos.
        <br><br>
        <span>Mendoza es un “ecosistema sano” en términos fitosanitarios; para transformar esta ventaja natural en una ventaja
        competitiva autogenerada, se han llevado adelante "Políticas de Estado", que han perdurado en el tiempo y han
        colocado a Mendoza a la vanguardia en esta materia.</span>
        <br><br>
        Estas políticas, prácticas e instituciones han mejorado el estatus fitosanitario de la provincia. Se ha buscado
        garantizar a través de ellas una condición mínima y necesaria para el acceso de los productos frutihortícolas
        provinciales a los diferentes mercados.
