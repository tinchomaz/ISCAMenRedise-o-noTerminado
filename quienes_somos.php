        producción agrícola en Mendoza, enfocada en los principales cultivos frutales como durazno, cereza, ciruela,
        pera, manzana, damasco y uva fresca, junto con la producción vitivinícola, alcanza la cifra de 186.351 millones
        de pesos.
        <br><span style="color: #336349;">Esta cantidad equivale, de manera estimativa, a unos U$S 512 millones de
