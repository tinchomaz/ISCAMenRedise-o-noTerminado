        A escala nacional, también es el primer exportador de ajo, nuez, durazno y frutas de carozo, y también es el
        primer productor durazno, ciruela y vid.
        <br><br>
        Los principales productos agroindustriales son: vino, mosto, aceite de oliva, durazno en conserva, ciruela
        desecada, y aceitunas en conserva. En Mendoza se elabora aproximadamente el 80% del vino argentino.
        <br><br>
