                análisis en laboratorios del INTA. Los resultados son informados generalmente en el mismo día.
                <br><br>
                Posteriormente, se libera el lote para su comercialización si los residuos detectados están comprendidos
                dentro de las tolerancias fijadas en normativas nacionales. Caso contrario, se decomisan para su
                destrucción
                y se labran las sanciones correspondientes.
