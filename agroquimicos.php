                <div class="col-12">
                    <span>Pasos del programa</span>
                    <br><br>
                    El ISCAMEN, a través del trabajo en campo, reparte en forma gratuita bolsones a los productores
                    agrícolas de toda la provincia. Estos deben colocar en los mismos los envases previamente lavados e
                    inutilizados. En este paso, se realiza el Triple Lavado del Envase, que permite la descontaminación
                    de
                    éste para su procesamiento
                    <br><br>
                    Cuando estos bolsones son completados e identificados, son entregados por los productores en los
                    Centros
                    de Almacenamiento Transitorio (CAT) de ISCAMEN o son llevados a las mismas casas expendedoras de
                    agroquímicos donde compraron el producto.
                    <br><br>
                    Al entregar envases previamente descontaminados para su procesamiento, los productores no solo
                    cumplen
                    con un requisito establecido por las Buenas Prácticas Agrícolas que puede ser certificado, sino
                    también
                    con el marco legal vigente.
                    <br><br>
                    ISCAMEN, através de sus CAT (Centro de Almacenamiento Transitorio) compacta los mismos, para su
                    posterior envío a la industria plástica y su trasformación y reutilización en productos utilizados
                    en el
                    agro como postes de vid, entre otros.
