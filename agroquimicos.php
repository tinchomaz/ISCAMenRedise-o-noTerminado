            ¿Qué son los
            <span>Agroquímicos?</span>
        </div>
        <div class="container d-flex flex-column align-items-center">
            <div class="col-12">
                El hombre siempre trató de proteger sus cultivos de los ataques de plagas y enfermedades que afectan los
                rendimientos y la calidad de los productos vegetales.
                <br><br>
                Para lograr este objetivo utilizó sustancias químicas naturales o artificiales, a las que hoy se
                denomina agroquímicos, en forma genérica. Según la región, el ámbito y los tiempos se las llama de
                diferentes maneras: fitosanitarios, fitoterapéuticos, fitofármacos, plaguicidas, pesticidas o
                agrotóxicos. Esta última denominación tiene una fuerte connotación negativa.
                <br><br>
                Pueden definirse como cualquier sustancia o mezcla de sustancias destinadasa prevenir, destruir y
                controlar cualquier organismo nocivo, incluyendo las especies no deseadas de plantas y animales que
                causan perjuicio o interfieren de cualquier otra forma en la producción, elaboración o almacenamiento de
                productos agrícolas. El término incluye coadyuvantes, fitorreguladores, desecantes y las sustancias
                aplicadas a los cultivos antes o después de la cosecha para proteger los productos vegetales contra el
                deterioro durante el almacenamiento y transporte.
                <br><br>
                Los agroquímicos constituyen un tema de múltiples facetas y de alta sensibilidad social. Si bien no se
                pueden desconocer los beneficios que han aportado a la agricultura; tampoco es posible relativizar el
                peligro que encierran debido a su toxicidad.
                <br><br>
                La tendencia actual es procurar reducir los riesgos adoptando enfoques preventivos mediante una fuerte
                asistencia técnica a los sectores de la producción, y una constante política de control para lo que se
                requieren recursos técnicos y económicos adecuados.
                <br><br>
                El ideal al que se debe aspirar es a un respeto de las normativas vigentes, políticas de
                capacitación y
                buen uso de las herramientas tecnológicas.
