                comercializados en Mendoza.
                <br><br>
                A través del Programa de Agroquímicos se toman muestras vegetales en búsqueda de residuos de
                agroquímicos
                que responden a diversos grupos químicos.Los más comunes son los organoclorados y fosforados, como
                también
                carbamatos y piretroides.
                <br><br>
                Las muestras vegetales se extraen sistemáticamente en ferias ,supermercados, acopiadores y empacadores.
                Al
