            Agroquímicos -
            <span>Campolimpio</span>
        </div>
        <div class="container">
            A través de “CampoLimpio” se aborda el problema delos envases vacíos de agroquímicos en la provincia. La
            gestión de envases de agroquímicos impulsa entre otras prácticas, el cuidado del medio ambiente y la salud
            humana a través del reciclado. Tiene como fin abandonar la cultura de arrojar los bidones y otros
            recipientes de agroquímicos en los rincones de las fincas.
            <br><br>
            En Mendoza, el programa de gestión de envases vacíos de agroquímicos, pionero en el país, comenzó a
            desarrollarse en el año 1998 bajo el Programa “Limpiemos el Campo”.
            <br><br>
            A nivel nacional, la Ley Nº 27.279, impulsada en 2016 por el modelo de ISCAMEN, establece los presupuestos
            mínimos de protección ambiental para la gestión de los envases vacíos de fitosanitarios, determinando la
            obligatoriedad de estas, siendo la Asociación CampoLimpio la encargada a nivel nacional de promocionar la
            gestión de envases. Mendoza adhiere al marco legal vigente y desarrolla acciones en forma conjunta con la
            Asociación en la provincia de Mendoza.
            <br><br>
            La normativa vigente establece que los productores están obligados a entregar los envases en los centros de
            acopio que se generanen la provincia, siendo el ISCAMEN y la Dirección de Protección Ambiental los
            organismos competentes para la aplicación de esta ley.
            <br><br>
            <div class="contenidoImg">
