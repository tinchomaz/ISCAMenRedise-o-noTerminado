            Triple
            <span>Lavado</span>
        </div>
        <div class="container">
            <div>
                <ul>
                    <li>Entre otras ventajas, la técnica del TRIPLE LAVADO permite aprovechar hasta la última gota del
                        producto y por lo tanto el productor ahorra dinero.</li>
                    <br>
                    <li>Cuando se termina el contenido de agroquímicos en cualquier tipo de envase (plásticos, metálicos
                        y/o vidrio) quedan restos del mismo que deben ser eliminados para evitar riesgos al hombre, los
                        animales y el medio ambiente.</li>
                    <br>
                    <li>Los envases vacíos deben ser totalmente escurridos en el momento de agotar su contenido; para
                        ello se debe mantener en posición de descarga durante no menosde 30 segundos, esto se evidencia
                        por un goteo espaciado.</li>
                    <br>
                    <li>Luego se debe llenar una cuarta parte del envase vacío con agua, se ajusta el tapón y se agita
                        enérgicamente. El agua proveniente de esta limpieza se agregará al tanque de la pulverizadora
                        para ser utilizado en la tarea fitosanitaria prevista.</li>
                    <br>
                    <li>Esta operación debe repetirse por lo menos dos veces más.</li>
                    <br>
                    <li>Se debe utilizar siempre agua proveniente de cañerías o canillas, nunca sesumergirán los envases
                        en acequias, cursos de agua o lagunas para su lavado yaque estas fuentes quedarían contaminadas.
                    </li>
                    <br>
                    <li>Una vez finalizada la tarea de aplicación, se debe inutilizar el envase perforándolo en el fondo
                        con un elemento punzante y colocarlo en la bolsa que le fue provista por el ISCAMEN o en la casa
                        de venta de agroquímicos.</li>
                    <br>
                    <li>Esta bolsa se colocará en un depósito transitorio el cual deberá estar ubicado en un sector
                        aislado del campo, muy bien de limitado e identificado, cubierto, bien ventilado y al resguardo
                        de factores climáticos.</li>
                    <br>
                    <li>Cuando se llena una bolsa, ésta debe ser trasladada al CAT más cercano</li>
                    <br>
