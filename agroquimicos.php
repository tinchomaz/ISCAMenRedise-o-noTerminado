        <div class="container">
            <div>
                <span>Huevos:</span> Miden algo menos de 1 mm y tienen forma redondeada o apenas alargada. Recién
                puestos,
                son traslúcidos, con brillo y reflejos tornasolados; luego se vuelven color amarillo. Se
                encuentran sobre los frutos en formación, donde parecen pequeñísimas gotas de cera apenas
                visibles.
                <br><br>
                <span>Larvas:</span> Se encuentran en los racimos y pueden medir entre 1 y 15 mm. Son de coloración
                verdosa
                (aunque varía de amarillo-verdoso a marrón-grisáceo), con la cabeza siempre de color marrón
                claro. Son móviles y ágiles y, al ser molestadas, se descuelgan por medio de un hilo sedoso.
                <br><br>
                <span>Pupas:</span> Son difíciles de localizar, ya que se esconden en la corteza de las cepas, en
                las hojas y
                en los racimos. Están envueltas por un capullo blanco, con brillo sedoso. Son alargadas, miden
                0,5 cmde largo y su color es marrón.
                <br><br>
                <span>Adulto:</span> Es una mariposa cuyo cuerpo mide alrededor de 6 mm. Sus alas, cuando están
                desplegadas,
                llegan a medir entre 10 y 13 mm. Su primer par de alas es jaspeado, con manchas oscuras en
                marrón alternando con zonas claras. Las alas posteriores son grises y están provistas de flecos
                grisáceos.
                <br><br>
                Una vez fecundadas las hembras de la primera generación, depositan los huevos aisladamente sobre
                la corola de los botones florales y las siguientes generaciones sobre los racimos. El incremento
                de los huevos tiene lugar al cabo de pocos días. La larva ataca los botones florales, uniéndolos
                con unos hilos sedosos, formando un “glomérulo” fácilmente reconocible. Al final del periodo
                larvario, la larva teje un capullo, en cuyo interior se forma la pupa o crisálida; casi la mitad
                lo hacen en repliegues de las hojas y el resto en la corteza de las cepas, racimos, suelo y
                otros. Al cabo de 5-10 días salen los nuevos adultos que repiten el ciclo, teniendo normalmente
                2 ó 3 generaciones al año, aunque pueden tener más, dependiendo de las condiciones climáticas y
                hospedero. En la 2ª y 3ª generación, la puesta tiene lugar sobre bayas verdes o en fase de
                maduración
