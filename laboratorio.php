            <div class="col-12 col-md-6 col-lg-6">
                <span>Detección e identificación de adultos</span>
                <br><br>
                Drosophila suzukii o mosca del vinagre de a las manchadas, es una especie de la familia
                Drosophilidae.Es polífaga con preferencia por los climas frescos y húmedos, pero con gran
                capacidad de adaptación a un amplio rango de condiciones climáticas.
                <br><br>
                A diferencia de otras drosophilas, posee un ovipositor aserrado que le permite cortar y
                depositar sus huevos en la piel de los frutos sanos.
                <br><br>
                El número de generaciones anuales es variable, dependiendo de las condiciones de temperatura y
                humedad del ambiente, pudiendo variar entre 7 y 15 generaciones por año (Kanzawa 1939; Mitsui et
                al., 2006; Walsh et al., 2011).
                <br><br>
                Una hembra adulta puede vivir de 20 a 30 días y poner entre 7 y 13 huevos por día, con una media
                de unos 384 huevos durante toda su vida.
                <br><br>
                En zonas con inviernos fríos, la forma invernante es el estado adulto con periodos de
                supervivencia de hasta 200 días. Con temperaturas menores de 5ºC, la hembra entra en día pausa
                reproductiva y no sale de este estado hasta que no se superan los 10ºC.
                <br><br>
                Se consideran hospederos de este insecto: frambuesas, zarzamoras, cerezas, ciruelas, arándanos,
                duraznos, frutilla, moras, higos, kiwis, kakis, entre otros.
                <br><br>
                <span>Análisis de trampas</span>
                <br><br>
                Para el seguimiento y testeo de una potencial incidencia de este insecto, se realiza el
                monitoreo a través de la lectura de trampas húmedas con vinagre como atrayente. Semanalmente se
                reciben, en promedio, 85 trampas pertenecientes a redes de monitoreo de SENASA y de la red
                oficial de ISCAMEN. Las mismas están distribuidas en los cuatro Oasis de la provincia.
                <br><br>
                El objetivo de este procedimiento es determina presencia de individuos adultos indicando si son
                machos o hembras.
