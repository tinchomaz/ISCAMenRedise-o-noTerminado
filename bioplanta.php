        <div class="container">
            Coexisten en la actualidad dos sistemas de liberación de Insectos Estériles
            <br><br>
            <span>Sistema convencional en bolsas de papel</span>
            <br><br>
            Las pupas de moscas del Mediterráneo esterilizadas se empacan en bolsas de papel, en un promedio de 4300
            machos estériles por bolsa aproximadamente, liberando diariamente 14.000 bolsas en todos los oasis
            productivos.
            <br><br>
            La bolsa contiene además papel soporte para permitir a los insectos desplegarse al momento de la emergencia
            y papel almibarado para proveerles de alimento a las moscas emergidas.
            <br><br>
            Luego de 24-36 hs. emergen las moscas de las pupas colocadas en el interior de las bolsas y comienzan a
            alimentarse y madurar.
            <br><br>
            Debido a que la madurez sexual del insecto estéril se alcanza a partir del quinto día, pero considerando que
            con el sistema tradicional de bolsas las moscas no reciben aporte de agua, éstas se deben liberar al tercer
            día; es decir dos días antes de madurar.
            <br><br>
            <span>Sistema de Adultos Fríos de mosca del Mediterráneo.</span>
            <br><br>
            El Sistema de Adulto Frío consiste en colocar las pupas esterilizadas en contenedores individuales con
            capacidadde hasta 55.000 pupas o capullos, apilables de hasta 20 unidades; conteniendo cada torre de
            emergencia hasta un millón de pupas o capullos por metro cuadrado.
            <br><br>
            Una vez que las moscas emergidas han alcanzado la madurez sexual óptima se procede a efectuar un shock de
            frío colocando las torres en cámaras refrigeradas a -2°C, por un lapso de 40 minutos. Durante este tiempo
            las moscas se aletargan lo que permite su separación y colocación en las cajas de liberación, estas además
            están refrigeradas lo que permite mantenerlas aletargadas todo el tiempo y así liberarlas al ambiente sin
            necesidad de bolsas ni otros soportes.
            <br><br>
            Las cajas de liberación son dispuestas en los aviones los cuales poseen dispositivos de dosificación
            regulables que distribuyen las moscas de acuerdo a las densidades establecidas para cada bloque de
            liberación.
            <br><br>
            El nuevo sistema permite liberar moscas hidratadas, alimentadas y en su madurez sexual; elimina del
            medioambiente los residuos de la liberación en bolsas considerando que por campañase liberan 3 millones de
            bolsas, aproximadamente.
            <br><br>
            Además, reduce los costos de liberación ya que se duplica la capacidad en igual tiempo de vuelo, permite
            optimizar la liberación considerando que en las épocas críticas de calor se acortan los tiempos de
            liberación y además se favorece la distribución y densidades a aplicar por hectárea.
