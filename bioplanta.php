            Etapas del proceso
            <span>de producción</span>
        </div>
        <div class="container">
            Cada una de las salas que componen la Bioplanta Multipropósito de producción de insectos estériles imita las
            condiciones naturales de la mosca del Mediterráneo con el propósito de que los insectos criados allí cumplan
            con todo su ciclo biológico natural (huevo, larva, pupa o capullo, adulto).
            <br><br>
            En cada etapa se reproducenlas condiciones de temperatura, humedad y luminosidad, del ciclo biológico enla
            naturaleza
            <br><br>
            Existe una colonia de insectos reproductores en jaulas diseñadas especialmente para que los huevos puestos
            por las hembras en telas de tul, caigan en canaletas con agua y puedan ser recolectados.
            <br><br>
            En la Sala de Incubación los huevos se mantienen burbujeando con aire en botellones con agua para
            uniformarlos nacimientos.
            <br><br>
            Cuando los huevos están a punto de eclosionar, se siembran sobre una dieta confeccionada a partir de
            levadura,harina de soja, azúcar, alfalfa molida, conservantes de uso alimenticio y acidulante, con la cual
            se alimentarán las larvas recién emergidas.
            <br><br>
            Los huevos sembrados en esa dieta son trasladados a la Sala de Iniciación Larvaria durante dos días; en esta
            Sala se imita el interior de una fruta madura, por lo tanto, las condiciones de temperatura son de 30ºC y la
            humedad es cercana al 100%.
            <br><br>
            Luego se colocan en la Sala deDesarrollo Larvario durante 4 o 5 días, aquí la temperatura se mantiene en27ºC
            y la humedad al 75%. Las larvas que han llegado al último estadío larval(maduras), saltan de las bandejas de
            dieta y se recolectan en recipientes conaserrín de madera de álamo.
            <br><br>
            En menos de 24 horas esas larvas comienzan a transformarse en pupas(capullos), y se trasladan a la Sala de
            Pupas bajo condiciones similares a lo que sucedería en la naturaleza aproximadamente a 3 cm bajo la
            superficie del suelo, con 22ºC de temperatura y 65% de humedad.
            <br><br>
            Cuando el futuro adulto se encuentra en el interior de la pupa comienza a diferenciar sus células sexuales y
            es en ese momento cuando se esterilizan.
            <br><br>
            La esterilización se realiza mediante un proceso utilizado y probado mundialmente que sólo afecta las
            células reproductivas (espermatozoides) de un insecto que pesa de promedio entre 7 y 8 miligramos, sin
            afectar su capacidad de desarrollo ni su instinto de reproducción dado que su propósito es que los machos
            esterilizados copulen a las hembras silvestres.
            <br><br>
            Actualmente la tecnología disponible (cepa termo sensible) permite separaren estado de huevo los machos de
            las hembras y por lo tanto criar solamente ejemplares machos de mosca del Mediterráneo haciendo más
            eficiente la TIE.
            <br><br>
            Una vez liberados, los machos estériles se aparean con las hembras silvestres las cuales no dejan
            descendencia, cortándose así el ciclo biológico natural.
