        <div class="tituloPrincipal">
            Envio de frutos hospederos
            <span>de mosca a Áreas Libres</span>
        </div>
        <div class="container contenidoImg">
            El Servicio Nacional de Calidad Agroalimentaria -SENASA- dispone la obligatoriedad de  cumplir con un
            Sistema de Mitigación de Riesgos para ingresar a las Áreas Libres de Moscas de los Frutos (ALMF) 
            <br><br>
            <ul>
                <li>Oasis Centro de Mendoza (Valle de Uco)</li>
                <li>Oasis Sur de Mendoza (San Rafael, General Alvear y Malargüe)</li>
                <li>Patagonia</li>
            </ul>

            El sistema de protección establecido contiene procedimientos que se ajustan a exigencias internacionales
            para proteger áreas libres. Involucra sólo a los productos vegetales susceptibles de ser atacados por moscas
            de los frutos (Ceratitis capitata - mosca del Mediterráneo- y  Anastrepha fraterculus).
            <br><br>
            Para garantizar el cumplimiento de los requisitos establecidos el Institutode Sanidad y Calidad Agropecuaria
            Mendoza estableció puestos de control interno de frutas provenientes de Áreas de Escasa Prevalencia de mosca
            del Mediterráneo (AEP), que vayan a ser comercializadas en fresco o industrializadas en establecimientos
            ubicados en Áreas Libres de la plaga.
            <br><br>
            Estos puestos están orientados principalmente al control de cargas comerciales destinadas a los Oasis Centro
            y Sur de la provincia como así también a la Patagonia.
            <br><br>
            <ul>
                <li>Zapata: Ruta Nacional 40, Km. 3.226, Tunuyán</li>
                <li>San José: Ruta Provincial 86, Km. 29, Tupungato</li>
                <li>Ñacuñán: Ruta Nacional 153, Km. 45, Departamento de Santa Rosa</li>
            </ul>
            Frutas producidas en los Oasis Norte y Este de Mendoza para comercialización en fresco en las áreas
            <span>Libres de moscas de los frutos (ALMF)</span>
            <br><br>
            <span>Etapa 1. Registro e inscripción</span>
            <br><br>
            Los productores deben inscribirse en el SMR con el número de RENSPA de la propiedad. La inscripción, que se
            actualizará cada año, debe realizarse en un plazo no mayor de 35 días (5 lecturas negativas obligatorias)
            antes del inicio de la cosecha de la especie que se destinará a consumo en fresco o a industrializar.
            <br><br>
            Las industrias, situadas en áreas libres, deberán formalizar también su inscripción 15 días antes de la
            recepción de la primera partida.
            <br><br>
            <span>Etapa 2: procedimiento de producción.</span>
            <br><br>
            En cualquier etapa de la producción (finca, empaque, centro de distribución y acopio/frigorífico), en todos
            los establecimientos agrícolas inscriptos en el SMR se realizará monitoreo (trampeo y muestreo) de la plaga.
            La normativa determina que ante la detección de un solo ejemplar de mosca de los frutos en las trampas, o la
            presencia de estados inmaduros de la plaga (huevos- larvas) al muestrear los frutos, el RENSPA QUEDARÁ
            INHABILITADO PARA realizar envíos de producto al ALMF
            <br><br>
            <span>Etapa 3. Cosecha.</span>
            <br><br>
            En el caso de no detectarse adultos en trampas o estados inmaduros en frutos, se autorizará la cosecha
            habilitándose el RENSPA en el SMR.
            <br><br>
            <span>Etapa 4: procedimiento transporte</span>
            <br><br>
            Para el despacho de una partida se hará un muestreo dirigido. Si no se detectan huevos o larvas se emitirá
            el certificado de habilitación de fruta para industria en áreas libres de moscas de los frutos (amparado
            Disposicion nacional SENASA 12/2016)
            <br><br>
            La carga se cubrirá con lona o malla (80% de cobertura) con soga única y deberá ser precintada.
            <br><br>
            Frutas producidas en los Oasis Norte y Este de Mendoza para industrialización en las áreas Libres de moscas
            de los frutos (ALMF)
            <br><br>
            <span>Establecimientos burbuja del SMR</span>
            <br><br>
            Son instalaciones (Empaque, Centro de distribución y acopio/frigorífico) que se encuentra monitorieadas   y cumplen con condiciones de resguardo para que la plaga no infecte  la materia prima o la mercadería procesada 
