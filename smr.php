            Exportación de frutas de pepita
            <span>a Brasil</span>
        </div>
        <div class="container contenidoImg">
            Para la República Federativa del Brasil la plaga Cydia pomonella - Carpocapsa -  es considerada plaga
            cuarentenaria A2, por lo que se debe cumplir con un Sistema de Mitigación de Riesgo (SMR) específico para
            manzanas, peras y membrillos de exportación producidos en Argentina.
            <br><br>
            Este sistema se implementa desde el año 2002 en las zonas de producción de frutales de pepita en la
            República Argentina y  establece distintas actividades a desarrollar que permiten un mayor control de la
            plaga en el campo, así como también acciones en los galpones de empaque que tienen por finalidad darle
            trazabilidada la fruta exportada.
            <br><br>
            SENASA es la organización nacional de protección fitosanitaria que garantiza el Sistema y el ISCAMEN lo
            implementa en Mendoza de acuerdo a los lineamientos impartidos.
            <br><br>
            <span>Etapa 1. Registro e inscripción:</span>
            <br><br>
            Inscripción de fincas y Unidades Productivas (UP), e identificación y delimitación de las mismas en el
            campo.
            <br><br>
            <span>Etapa 2. Procedimiento de producción:</span>
            <br><br>
            Implementación de la Técnica de Confusión Sexual (TCS), obligatoria en el 100 % de la superficie inscripta.
            Establecimiento de una red de trampeo con una densidad de una trampa de feromona 10x o combo cada 1.5 hs y
            registro de capturas en cuaderno de campo y Sigtraza (SENASA). Calibración obligatoria del equipo de
            pulverización. Registro en cuaderno de campo de aplicación fitosanitarias y labores culturales.
            <br><br>
            <span>Etapa 3. Procedimiento de precosecha</span>
            <br><br>
            Realización de reporte de daño en cada UP (Unidad Productiva), con corte del 100 % de la muestra, debiendo
            ser el daño inferior al 1 %.
            <br><br>
            <span>Etapa 4. Cosecha:</span>
            <br><br>
            Mantener trazabilidad de la fruta mediante la identificación en cosechay en galpón de empaque.
            <br><br>
            <span>Etapa 5. Procedimiento a seguir en poscosecha:</span>
            <br><br>
            Ingreso de fruta al galpón de empaque con menos 0.5 % de daño. Extracción de muestra en ingreso a galpón de
            empaque (0.2% de los kilos ingresados), y corte del 100% de la muestra en la cual si se detecta larva viva
            de Carpocapsa se rechaza la UP para ser exportada a Brasil. Mantener trazabilidad de la fruta.
            <br><br>
            <span>Etapa 6. Inspección y certificación final SENASA</span>
