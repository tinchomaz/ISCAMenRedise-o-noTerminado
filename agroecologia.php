            <div class="col-12 mb-1">
                El Control Biológico Integrado es una herramienta fundamental del Manejo Integrado de Plagas (MIP) que
                permite disminuir la presencia de insectos-plaga mediante la utilización de sus propios enemigos
                naturales; es decir, a través de la acción de distintos insectos predadores, parásitos y patógenos.
                <br><br>
                <span>Parasitoides</span>
                <br>
                Son insectos que viven a expensas de otro insecto (hospedero) al que devoran progresivamente hasta
                causarle la muerte. Mientras tanto, completan su propio desarrollo y emergen de los restos de sus
                hospederos.
                <br><br>
                <span>Predadores</span>
                <br>
                Son insectos u otros animales que matan a las plagas succionándoles la sangre o devorándolos.
                <br><br>
                Los parásitos y predadores buscan a sus hospederos y presas en lugares donde éstos se encuentran,
                incluyendo sus refugios. Su acción tiende a incrementarse en los momentos en que las poblaciones de
                plagas son altas.
                <br><br>
                El Control Biológico Integrado tiene características propias que lo distinguen de otras formas de
                combate de plagas, particularmente del uso de químicos, mejorando significativamente la calidad e
                inocuidad de la fruta obtenida.  Se puede combinar con otras herramientas de control como por ejemplo la
                Técnica del Insecto Estéril (TIE).
                <br><br>
                En los laboratorios de Km8 - Guaymallén, Oasis S ur, Valle de Uco y Este, se realizan las tareas de
                producción masiva y liberación de parasitoides y predadores.
