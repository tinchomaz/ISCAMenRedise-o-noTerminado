            <span>Fiscalización de papa semilla</span>
            <br><br>
            La papa semilla es de fiscalización obligatoria, esto significa que todo aquel que produzca esta especie
            debe inscribir el cultivo para someterlo al control oficial, por parte de los organismos competentes. 
            <br><br>
            Esta regulación legal específica está dada por la Resolución 249/98 que fue puesta en vigencia para la
            campaña 99/00.
            <br><br>
            En nuestra provincia la zona específica para la producción de semilla de papa fiscalizada es Malargüe.
            <br><br>
            <span>MALARGÜE: Área protegida para la producción de de papa semilla</span>
            <br><br>
            Legislación:  la ley provincia l5326/88 que declara a Malargüe Área Diferenciada para la Producción de Papa
            Semilla Fiscalizada y su Decreto Reglamentario 1921, establece normas que han posibilitado el control del
            ingreso y la difusión de patógenos y plagas de la papa.
            <br><br>
            <span>Produciendo tubérculos de papa semilla</span>
            <br><br>
            Los productores de semilla de papa de la zona desarrollan los cultivos enforma muy intensiva y disponiendo
            de tecnología de punta. Las siembras se realizan utilizando unos 3.000 Kg. de tubérculos semilla por
            hectárea, una alta densidad que busca aumentar el número de éstos y disminuir su tamaño. La cosecha se
            realiza principalmente durante el mes de abril y la producción total oscila alrededor de 25.000 toneladas
            por año.
            <br><br>
            Debido a las excelentes condiciones sanitarias de la zona, no es necesario hacer uso de biocidas
            (insecticidas y fungicidas) que se utilizan en todo el mundo para lograr cultivos económicamente rentables.
            <br><br>
            Actualmente Malargüe tiene excelente producción de las cultivares  Spunta principalmente y en pocas
            proporciones Kennebec, Innovater y Atlantic.
            <br><br>
            Es de destacar que en el Área Diferenciada Malargüe sólo se permite la siembra de materiales originados
            dentro de la misma. La excepción la constituye el caso de materiales prebásicos producidos bajo condiciones
            controladas en laboratorios autorizados a tal fin.
            <br><br>
            Con el objeto de preservar la condición sanitaria distintiva de Malargüe,ha sido necesario implementar
            medidas como el control de los materiales vegetales que ingresan al departamento. Esto se realiza por medio
            de un sistema de Barreras Sanitarias que funcionan bajo la órbita del ISCAMEN e INASE.MALARGÜE.
            <br><br>
            Se fiscalizan aproximadamente 800 has por ciclo agrícola. La fiscalización incluye por lo menos tres
            inspecciones en el lote registrado. En la primera se verifica el origen de la semilla y las condiciones de
            siembra en general, en una segunda instancia se controla el estado sanitario de los cultivos determinando
            porcentuales de los síntomas establecidos en la normativa, y en la tercer inspección se extraen muestras
            para realizar pruebas postcosecha. La fiscalización concluye con el pedido de estampillas y el control de
            comercio de la semilla.
            <br><br>
            Se controla, además, la rotación de los lotes mediante la utilización de GPS y la presencia de papa
            espontánea.
            <br><br>
            Es importante aclarar que, en semilla de papa, se produce una caída natural de categoría de una
            multiplicación a otra, aunque las pruebas de sanidad anticipada hayan arrojado resultados que se encuadren
            dentro de las tolerancias de una categoría superior.

            En la actualidad también se fiscalizan 500 ha de Ajo del tipo colorado, 76ha de blanco y 53 ha de “chino” o
            morado.
            <br><br>
            <span>Control en plantines de frutillas, cebolla y ajo</span>
            <br><br>
            A pedido de productores que desean introducir plantines al área protegida y en concordancia con el Comité
            Técnico de papa semilla, se establecen sistemas de mitigación de riesgos para el ingreso, realizando
            muestreos de plantines, bulbos y suelo que se envían a laboratorio para realizar análisis nematologicos y
            fitopatológicos, garantizando la inocuidad de los mismos, lo que permite el ingreso a Malargüe.
            <br><br>
            La producción de materiales de propagación es, sin dudas ,una típica actividad basada en las condiciones
            fitosanitarias como ventaja comparativa.
            <br><br>
            El ISCAMEN, en convenio con el Instituto Nacional de Semillas, es quien aplica en la provincia la Ley
            Nacional de Semillas y Creaciones Fitogenéticas Nº 20247/73 y su decreto reglamentario, cuyo objeto
            es"promover una eficiente actividad de producción y comercialización de semillas. Para esto se efectúan
            controles periódicos de viveros, realizándose inspecciones sanitarias, relevamiento y verificación de
            existencias y controles de comercio a la hora de la venta de plantas. Además se controla el ingreso, egreso
            y tránsito en la provincia de plantas de vivero.
            <br><br>
            Tanto INASE, INTA de Junín e ISCAMEN han permitido que actualmente Mendoza  esté en condiciones de comenzar
            a producir plantas certificadas de frutales de carozo "libresde virus" (aquellos económicamente
            perjudiciales). La certificación de plantas de vivero posibilita la exportación; además, genera un clima
            deconfianza para los creadores de variedades, permitiendo a los viveristas acceder a la oferta varietal
            internacional.
