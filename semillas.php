            Agropecuaria Mendoza (ISCAMEN). En tanto, que a nivel provincial, la institución realiza acciones en el
            marco de normativas específicas para casos especiales como por ejemplo la LeyProvincial de Área Protegida
            Malargüe Nº 5326.
            Para cumplir con dichos objetivos la misma ley crea los siguientes registros:
