            La regulación relativa a semillas está dada por la Ley Nacional de Semillas y Creaciones Fitogenéticas Ley
            20247/73  y sus decretos reglamentarios.
            Esta ley se origina en la necesaria intervención del Estado en temas de fiscalización y control de semillas;
            además, se requería la intervención pública en la protección de la propiedad de cultivares y en otros temas
            relacionados con políticas agrícolas relativas a semillas.
            <br><br>
            Desde la sanción de esta ley, se ha observado un crecimiento marcado en las exportaciones de materiales de
            propagación de distintas especies, mayormente de cereales y oleaginosas. 
            <br><br>
            Tiene por objeto promover una eficiente actividad de producción y comercialización de semillas, asegurar a
